def parse_date(val):
    if val is None: return None
    if isinstance(val, datetime): return val
    for fmt in ('%Y-%m-%d %H:%M:%S','%Y-%m-%d %H:%M:%S.%f','%Y-%m-%d'):
        try: return datetime.strptime(str(val), fmt)
        except ValueError: pass
    return None

