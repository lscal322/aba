# ── SQLite cursor wrapper ─────────────────────────────────────────────────────
class SQLiteCursor:
    def __init__(self, cur):
        self._cur = cur
        self._lastrowid = None

    def execute(self, sql, params=None):
        has_ret = bool(re.search(r'\bRETURNING\b', sql, re.IGNORECASE))
        if has_ret:
            sql = re.sub(r'\s+RETURNING\s+\w+', '', sql, flags=re.IGNORECASE)
        sql = sql.replace('%s', '?')
        self._cur.execute(sql, params or [])
        self._lastrowid = self._cur.lastrowid
        self._returning = has_ret

    def fetchone(self):
        if getattr(self, '_returning', False):
            return {'id': self._lastrowid}
        row = self._cur.fetchone()
        return dict(row) if row else None

    def fetchall(self):
        return [dict(r) for r in self._cur.fetchall()]

def get_db():
    if 'db' not in g:
        if USE_SQLITE:
            conn = sqlite3.connect(SQLITE_PATH)
            conn.row_factory = sqlite3.Row
            g.db = conn; g.use_sqlite = True
        else:
            conn = psycopg2.connect(DATABASE_URL, cursor_factory=psycopg2.extras.RealDictCursor)
            conn.autocommit = False
            g.db = conn; g.use_sqlite = False
    return g.db

def get_cursor():
    db = get_db()
    cur = db.cursor()
    return SQLiteCursor(cur) if g.get('use_sqlite') else cur

