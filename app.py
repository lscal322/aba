def get_current_user():
    uid = session.get('user_id')
    if not uid: return None
    try:
        cur = get_cursor()
        cur.execute('SELECT id,name,email,xp,tests_completed FROM users WHERE id=%s',(uid,))
        return cur.fetchone()
    except: return None

def login_required(f):
    from functools import wraps
    @wraps(f)
    def decorated(*args, **kwargs):
        if not session.get('user_id'):
            return redirect('/py/login')
        return f(*args, **kwargs)
    return decorated

def add_to_mistake_bank(user_id, question_id, wrong_idx):
    try:
        db = get_db(); cur = get_cursor()
        cur.execute('SELECT id,review_count FROM mistake_bank WHERE user_id=%s AND question_id=%s',(user_id,question_id))
        existing = cur.fetchone()
        if existing:
            cur.execute('UPDATE mistake_bank SET wrong_answer_idx=%s, review_count=review_count+1, next_review=%s WHERE id=%s',
                       (wrong_idx, datetime.now().strftime('%Y-%m-%d %H:%M:%S'), existing['id']))
        else:
            cur.execute('INSERT INTO mistake_bank(user_id,question_id,wrong_answer_idx) VALUES(%s,%s,%s)',
                       (user_id,question_id,wrong_idx))
        db.commit()
    except: pass

