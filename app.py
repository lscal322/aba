    answers=data.get('answers',{})
    sess_id=data.get('sess_id',0)
    times=data.get('times',{})
    qids=[int(k) for k in answers if str(k).isdigit()]
    if not qids: return jsonify({'score':0,'total':0,'results':[]})
    try:
        db=get_db(); cur=get_cursor()
        ph=','.join(['%s']*len(qids))
        cur.execute(f'SELECT * FROM questions WHERE id IN ({ph})',qids)
        qmap={q['id']:q for q in cur.fetchall()}
        correct=0; results=[]; slow_tasks=[]
        for qid_s,ans in answers.items():
            try:
                qid=int(qid_s); q=qmap.get(qid)
                if not q: continue
                try: ai=int(ans)
                except: ai=-1
                is_c=ai==q['correct_index']
                if is_c: correct+=1
                else: add_to_mistake_bank(user['id'],qid,ai)
                t=int(times.get(qid_s,0))
                if t>300: slow_tasks.append(q['task_number'])
                opts=json.loads(q.get('options','[]'))
                try: we=json.loads(q.get('wrong_explanations') or '{}')
                except: we={}
                results.append({'id':qid,'correct':is_c,'task_number':q['task_number'],
                    'correct_index':q['correct_index'],'explanation':q['explanation'] or '',
                    'wrong_explanation':we.get(str(ai),'') if not is_c else '',
                    'correct_option_text':opts[q['correct_index']] if opts else ''})
                cur.execute('INSERT INTO question_attempts(user_id,question_id,answer_idx,is_correct,time_sec,mode) VALUES(%s,%s,%s,%s,%s,%s)',
                           (user['id'],qid,ai,1 if is_c else 0,t,'exam'))
            except: continue
        score=round(correct/len(qids)*100) if qids else 0
        xp=correct*15
        cur.execute('INSERT INTO test_results(user_id,subject,score,questions_count,mode) VALUES(%s,%s,%s,%s,%s)',
                   (user['id'],subject,score,len(qids),'exam'))
        cur.execute('UPDATE users SET xp=xp+%s,tests_completed=tests_completed+1 WHERE id=%s',(xp,user['id']))
        if sess_id:
            cur.execute('UPDATE exam_sessions SET finished_at=%s,score=%s,correct=%s,total=%s WHERE id=%s',
                       (datetime.now().strftime('%Y-%m-%d %H:%M:%S'),score,correct,len(qids),sess_id))
        db.commit()
        return jsonify({'score':score,'correct':correct,'total':len(qids),'xp_gain':xp,'results':results,'slow_tasks':slow_tasks})
    except Exception as e:
        try: get_db().rollback()
        except: pass
        return jsonify({'error':str(e)}),500

